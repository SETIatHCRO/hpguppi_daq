#ifndef BLADE_ATA_MODE_B_CONFIG_H
#define BLADE_ATA_MODE_B_CONFIG_H

#define BLADE_ATA_MODE_B_CIRCULAR_POLARIZATION false
#define BLADE_ATA_MODE_B_CHANNELIZER_RATE 1 // [1, 4]; <= 1 mitigates the channlisation

#define BLADE_ATA_MODE_B_INPUT_NANT 28
#define BLADE_ATA_MODE_B_INPUT_NCOMPLEX_BYTES 2

#define BLADE_ATA_MODE_B_ANT_NCHAN 192
#define BLADE_ATA_MODE_B_NTIME 8192
#define BLADE_ATA_MODE_B_NPOL 2

#define BLADE_ATA_MODE_B_OUTPUT_NBEAM 1
#define BLADE_ATA_MODE_B_OUTPUT_NCOMPLEX_BYTES 4

#define BLADE_ATA_MODE_B_OUTPUT_MEMCPY2D_PAD 0 // zero makes memcpy2D effectively 1D
#define BLADE_ATA_MODE_B_OUTPUT_MEMCPY2D_WIDTH 8192

#if BLADE_ATA_MODE_B_OUTPUT_NCOMPLEX_BYTES == 8
	#define BLADE_ATA_MODE_B_OUTPUT_ELEMENT_T CF32
#else
	#define BLADE_ATA_MODE_B_OUTPUT_ELEMENT_T CF16
#endif

#define BLADE_ATA_MODE_B_OUTPUT_MEMCPY2D_DPITCH (BLADE_ATA_MODE_B_OUTPUT_MEMCPY2D_WIDTH+BLADE_ATA_MODE_B_OUTPUT_MEMCPY2D_PAD)

#define BLADE_ATA_MODE_B_OUTPUT_DATA_SIZE (BLADE_ATA_MODE_B_OUTPUT_NBEAM *\
                               BLADE_ATA_MODE_B_ANT_NCHAN *\
                               BLADE_ATA_MODE_B_NTIME *\
                               BLADE_ATA_MODE_B_NPOL *\
                               BLADE_ATA_MODE_B_OUTPUT_NCOMPLEX_BYTES)

#define BLADE_ATA_MODE_B_DATA_SIZE ((BLADE_ATA_MODE_B_OUTPUT_DATA_SIZE/BLADE_ATA_MODE_B_OUTPUT_MEMCPY2D_WIDTH)*BLADE_ATA_MODE_B_OUTPUT_MEMCPY2D_DPITCH)

#endif